    function mulaiwaktu(){
        timerInterval = setInterval(() => {
            if(waktutersisa > 0){
                waktutersisa--;
                const menit = Math.floor(waktutersisa / 60);
                const detik = waktutersisa % 60;
                waktu.textContent = `${String(menit).padStart(2, "0")}:${String(detik).padStart(2, "0")}`;
            }else{
                clearInterval(timerInterval);
                TampillNilai();
            }
        },1000)
    }
    
